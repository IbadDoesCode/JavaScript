// Must-know JS Dev's Functions

// 1. document.getElementById()
let element1 = document
.getElementById('myElement')
// this function is used to select
// specific HTML element using its
// ID so you can change its text
// or style using JS.

// 2. querySelector()
let element2 = document
.querySelector(".myClass")
// it selects HTML element based
// on a CSS selector.

// 3. querySelectorAll()
let element3 = document
.querySelectorAll(".myClass")
// it returns all elements that
// matches CSS selector.
